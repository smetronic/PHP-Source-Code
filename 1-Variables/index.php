<?php //PHP Code Block

/**
 * ---------------------------------------------------------------------------------------
 * PHP Block
 * 
 * Put PHP code in the PHP block to execute
 * ---------------------------------------------------------------------------------------
 */

/**
 * ---------------------------------------------------------------------------------------
 * Variables start with a $ Symbol
 *
 * #Variables are used to Store and Get data
 * ---------------------------------------------------------------------------------------
 */
$name = "Alexander";

/**
 * ---------------------------------------------------------------------------------------
 * Print the variable value to screen
 * ---------------------------------------------------------------------------------------
 */
echo $name;

/**
 * ---------------------------------------------------------------------------------------
 * Print the string value to screen
 * ---------------------------------------------------------------------------------------
 */
echo 'Hello Alexander';

/**
 * ---------------------------------------------------------------------------------------
 * Print the string value and concatenate it with a variable
 * ---------------------------------------------------------------------------------------
 */
echo 'Hello ' . $name;

/**
 * ---------------------------------------------------------------------------------------
 * Print the string value and the variable value inside the double quotes
 * ---------------------------------------------------------------------------------------
 */
echo "Hello $name";

/**
 * ---------------------------------------------------------------------------------------
 * Print the variable value with the HTML Code
 * ---------------------------------------------------------------------------------------
 */
echo '<strong>Hello' . $name . '</strong>';

 //PHP End Block ?>